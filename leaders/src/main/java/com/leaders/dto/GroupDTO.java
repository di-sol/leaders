package com.leaders.dto;

public class GroupDTO {
	
	private int groupNum;
	private String groupName;
	private String groupServer;
	private String groupPriority;
	
	
	public int getGroupNum() {
		return groupNum;
	}
	public void setGroupNum(int groupNum) {
		this.groupNum = groupNum;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getGroupServer() {
		return groupServer;
	}
	public void setGroupServer(String groupServer) {
		this.groupServer = groupServer;
	}
	public String getGroupPriority() {
		return groupPriority;
	}
	public void setGroupPriority(String groupPriority) {
		this.groupPriority = groupPriority;
	}

}
